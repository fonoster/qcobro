export type Language = keyof typeof messages;
export type MessageId = keyof (typeof messages)[Language];

export const languages = Object.keys(messages) as Language[];
export const defaultLanguage: Language = "es";

/** Endonyms — each language shown in its own name, regardless of the active UI language. */
export const languageNames: Record<Language, string> = {
  en: "English",
  es: "Español"
};

const LANGUAGE_STORAGE_KEY = "qcobro.language";

function isLanguage(value: string | null): value is Language {
  return value !== null && (languages as string[]).includes(value);
}

/** The language to render before the server preference loads: the cached choice, else default. */
export function readStoredLanguage(): Language {
  try {
    const cached = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    if (isLanguage(cached)) return cached;
  } catch {
    // localStorage unavailable (SSR/private mode) — fall through to the default.
  }
  return defaultLanguage;
}

interface I18nContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (id: MessageId) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({
  children,
  initialLanguage = readStoredLanguage()
}: {
  children: ReactNode;
  initialLanguage?: Language;
}) {
  const [language, setLanguageState] = useState<Language>(initialLanguage);

  // Changing language updates the UI immediately and caches the choice so the next load
  // renders it without flashing the default (the server profile remains the source of truth).
  const setLanguage = (next: Language) => {
    setLanguageState(next);
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, next);
    } catch {
      // Ignore cache write failures; in-memory state still applies for this session.
    }
  };

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (id) => messages[language][id]
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used within an <I18nProvider>");
  }
  return ctx;
}
